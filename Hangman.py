#task 1 - Randomly choosing a word.

chosen_word = random.choice(word_list)

#task - 2 : Tracking the lives 

lives = 6

# task 3 - Create an empty List called display.
# #For each letter in the chosen_word, add a "_" to 'display'.
# #So if the chosen_word was "apple", display should be 
# ["_", "_", "_", "_", "_"] with 5 "_" representing each letter
# to guess.


display = []
for _ in range(len(chosen_word)):
    display += "_"

#task - 4 : Create a while loop to allow the user enter multiple
#inputs . The loop has to stop once the user guessed all the
#characters.
print(logo)
end_of_game = False
while not end_of_game:
    guess = input("Guess a letter: ").lower()
    # task 4-1 - Loop through each position in the chosen_word;
    # #If the letter at that position matches 'guess' then reveal
    # that letter in the display at that position.e.g. If the user 
    # guessed "p" and the chosen word was "apple", then display should 
    # be ["_", "p", "p", "_", "_"].

    #task - 4-2 : If the user enters a letter that they have already
    #guessed right, print the letter and let them know.

    if guess in display:
        print(f"You have already guessed {guess} ")

    for index in range(len(chosen_word)):
        if chosen_word[index] == guess:
            display[index] = chosen_word[index]
            print(f"Current position :{index}\n current letter :{guess}")
    # task 2-1 - deducting lives if the letter is wrong
    if guess not in chosen_word:
        print("You've guessed the letter that's not in the word")
        lives -= 1
        if lives == 0:
            end_of_game = True
            print("You Lose")
    print(display)
    if "_" not in display:
        end_of_game = True
        print("You Win!")

    #task - 5:

    print(stages[lives])
     

